													<p>Shri Jagat Prakash Nadda was born on December 2,
1960, to Dr. Narain Lal Nadda and Shrimati Krishna Nadda. He received his early education at St. Xavier's
School, Patna. His father was a professor at Patna
University and later became the Vice-Chancellor of
Ranchi University. As a child, he participated in the relay
fast as part of the J.P. movement against then Congress
regime of Bihar.</p>
<p>Shri Nadda completed his B.A. from Patna College,
Patna University, and obtained an LLB from Himachal Pradesh University, Shimla. 
He became active in the Akhil Bharatiya Vidyarthi Parishad (ABVP) in Patna and
played a significant role in the student movement during the Jai Prakash Narayan movement of 1975.</p>

<p>He was elected as the President of the Student Central
Association of Himachal Pradesh University in the year
1983. He became a full-time activist of ABVP in 1985.
During the period from 1985 to 1990, he held various
positions in ABVP including National Secretary and
Organising Secretary of Delhi Metropolitan Council. He
also led various student movements through the
Rashtriya Sangharsh Morcha during this period.</p>
<p>In 1990, he joined the Bharatiya Janta Party (BJP) as the
General Secretary of Himachal Pradesh BJP. In 1991, he
was given the responsibility of National president of the
Bharatiya Janata Yuva Morcha and worked under the
leadership of the then National President, Shri Murali
Manohar Joshi, participating with the youth during the
Ekta Yatra. He was re-elected in 1993 and also served
under the leadership of Shri Lal Krishna Adwani.</p>
<p>In 1993, Shri Nadda was elected as an MLA from
Bilaspur, Himachal Pradesh, and became the Leader of
the Opposition in the Himachal Pradesh Assembly. He

was awarded the best legislator in 1995. He was re-
elected as an MLA from 1998 to 2003 and served as the

Minister of Health and Family Welfare and
Parliamentary Affairs in the Himachal Pradesh State
Government. In 2OO7 , he was elected as an MLA for the
third time and became the Minister of Forest,
Environment; Science and Technology; Law and
Parliamentary affairs</p>
<p>In 2010, Shri Nadda became the National General Secretary of the BJP after resigning from the Council of Ministers in Himachal Pradesh. Working under the then
National President Shri Nitin Gadkari, he was in charge
of the Central Office and served as Election in-charge for
several states.</p>
<p>In 2012, he was elected a member of the Rajya Sabha
from Himachal Pradesh. In 2013, he was re-elected as
National General Secretary. In 2014, he became
Secretary of the Central Election Committee and was
elected National General Secretary for the third time
under the leadership of Shri Amit Shah. He was also
appointed as Secretary of the BJP Parliamentary Board,
the highest decision-making body of the party.</p>
<p>In November 2014, Shri Nadda became the Union
Minister of Health in the PM Shri Narendra Modi
government. He was instrumental in launching the
Ayushman Bharat Scheme, providing free health
insurance of up to five lakh rupees annually to about 55
crore people. He also improved the healthcare system
by ensuring the availability of medicines, opening Jan
Aushadhi Kendras, and digitising hospitals.</p>
<p>Shri Nadda was re-elected to the Rajya Sabha in 2018
from Himachal Pradesh. After the BJP's success in the
2019 Lok Sabha elections, he was appointed the
National Working President of the BJP on June 17, 2O19,
to share responsibilities with National President Amit
Shah, who took the role of Union Home Minister. ln
2019, he was made the election in-charge of the State
of Uttar Pradesh. On 20 January 2020, he was elected
unanimously as the BJP National President.</p>
<p>Under his leadership, the BJP achieved significant
success in various elections, including the Kashmir BDC
elections and the GHMC elections in Hyderabad, as well
as local body elections in several states like Assam,
Tripura, Arunachal Pradesh, Rajasthan, Uttar Pradesh,
Gujarat, Goa, Ladakh and Karnataka. The party also
broke records in Legislative Assembly elections in
Gujarat, Uttar Pradesh, Uttarakhand, Goa, Manipur, West Bengal, Assam, Chhattisgarh, Rajasthan, and
Madhya Pradesh.</p>
<p>During his tenure, the BJP launched a massive
membership drive, bringing in over 180 million new
members. During the Covid-19 pandemic, Shri Nadda
initiated the 'Sewa Hi Sangathan' mission, distributing over 220 million food kits, 50 million ration kits, and 56.6 million face masks across over 900 districts in lndia.</p>
<p>On January 17, 2023, during the National Executive
Meeting in New Delhi, Shri Nadda's tenure as National
President of the BJP was extended until June 2024. He
was elected unopposed to the Rajya Sabha from Gujarat
and took oath on April 6, 2024. On June LL, 2024, Shri
J.P. Nadda assumed the role of Union Minister of Health
and Family Welfare and Minister of Chemicals and
Fertilizers.</p>
